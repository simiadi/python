def sumOfTwoNumbers(a: float, b: float) -> float:
    result: float = None
    result = a + b
    return result

def kivonas(a: float, b: float) -> float:
    result: float = None
    result = a - b
    return result

def szorzas(a: float, b: float) -> float:
    result: float = None
    result = a * b
    return result

def osztas(a: float, b: float) -> float:
    result: float = None
    result = a / b
    return result