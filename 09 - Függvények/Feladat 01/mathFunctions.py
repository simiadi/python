def sumOfTwoNumbers(a: float, b: float) -> float:
    result: float = None
    result = a + b
    return result

def extractionOfTwoNumbers(a: float, b: float) -> float:
    result: float = None
    result = a - b
    return result

def multiplicationOfTwoNumbers(a: float, b: float) -> float:
    result: float = None
    result = a * b
    return result

def divisionOfTwoNumbers(a: float, b: float) -> float:
    result: float = None
    result = a / b
    return result